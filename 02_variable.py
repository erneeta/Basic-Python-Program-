# Rules for variable name
# 1. Consist of alphabets, digit(numbers), underscore
# 2. Starts with alphabet or underscore _
# 3. Python keywords are not allowed
# 4. Whitespace not allowed in between 
# 5. Case sensitive


# When variable consist of more than 1 word then following ways to declare such variable
