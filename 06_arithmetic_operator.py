# ARITHMETIC OPERATOR
a = 98
b = 2

print(a+b) # Addition

print(a-b) # Subtraction

print(a*b) # Multiplication

print(a/b) # Division

print(a**b) # Exponent (power)

print(a%b) # modulus (returns remainder)

print(a//b) # Floor Division ( returns division result but remove part after decimal)